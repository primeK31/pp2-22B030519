#upper case
a = "Hello, World!"
print(a.upper())

#lower case
a = "Hello, World!"
print(a.lower())

#Remove Whitespace
a = " Hello, World! "
print(a.strip()) #"Hello, World!"

#Replace String
a = "Hello, World!"
print(a.replace("H", "J")) #Jello, World!

#Split String
a = "Hello, World!"
print(a.split(",")) #['Hello', ' World!']